class BasicsDemo {

  public static void main(String[] args) {

    int i = 6;
    int j = 2;

    System.out.println("Some math to practice Java!");
    System.out.print("i + j = ");
    System.out.println(i + j);
    System.out.print("i - j = ");
    System.out.println(i - j);
    System.out.print("i * j = ");
    System.out.println(i * j);
    System.out.print("i / j = ");
    System.out.println(i / j);
    System.out.print("i % j = ");
    System.out.println(i % j);
    System.out.println("% means the remainder of the division. In this case it is 0.");

  }
}
